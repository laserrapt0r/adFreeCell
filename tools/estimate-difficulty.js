/* adFreeCell - fast heuristic difficulty ESTIMATE for all 32000 deals.
 *
 * A true solver-verified rating of all 32000 is impractical (FreeCell needs a
 * dedicated solver; a full best-first pass takes hours and scales poorly). This
 * instead scores each deal from cheap static features of the opening layout
 * (how deeply the aces / low cards are buried, foundations blocked, initial
 * ordered runs) and buckets the scores into tiers. It is an ESTIMATE, computed
 * in seconds. We validate that it correlates with real solver effort on a
 * sample and print the rank correlation.
 *
 * Usage: node tools/estimate-difficulty.js   ->  writes js/difficulty.js
 */
'use strict';
const fs = require('fs');
const path = require('path');
global.window = global;
require(path.join(__dirname, '..', 'js', 'deal.js'));
const D = window.FreeCellDeal;
const { solve } = require('./rate-difficulty.js');

const MAX = 32000, UNSOLVABLE = 11982;

// feature score for one deal (higher = harder)
function featureScore(n) {
  const cols = D.deal(n).columns; // each col: [{suit,rank}] top(0)->accessible(last)
  let buried = 0, aceDepth = 0, runBonus = 0;
  for (let k = 0; k < 8; k++) {
    const col = cols[k], L = col.length;
    for (let d = 0; d < L; d++) {
      const c = col[d], depth = L - 1 - d; // cards on top of this one
      buried += depth * (14 - c.rank);      // buried low cards weigh most
      if (c.rank === 1) aceDepth += depth;  // buried aces are the classic pain
    }
    // length of the ordered (descending, alternating-colour) run at the accessible end
    let run = 1;
    for (let d = L - 1; d > 0; d--) {
      const a = col[d], b = col[d - 1];
      const aRed = a.suit === 1 || a.suit === 2, bRed = b.suit === 1 || b.suit === 2;
      if (b.rank === a.rank + 1 && bRed !== aRed) run++; else break;
    }
    runBonus += run - 1;
  }
  return buried + 6 * aceDepth - 3 * runBonus;
}

// ---- validate: rank-correlation vs solver effort on a sample ----
function spearman(a, b) {
  const rank = arr => {
    const idx = arr.map((v, i) => [v, i]).sort((x, y) => x[0] - y[0]);
    const r = new Array(arr.length);
    for (let i = 0; i < idx.length; i++) r[idx[i][1]] = i;
    return r;
  };
  const ra = rank(a), rb = rank(b), N = a.length;
  const mean = x => x.reduce((s, v) => s + v, 0) / x.length;
  const ma = mean(ra), mb = mean(rb);
  let num = 0, da = 0, db = 0;
  for (let i = 0; i < N; i++) { const x = ra[i] - ma, y = rb[i] - mb; num += x * y; da += x * x; db += y * y; }
  return num / Math.sqrt(da * db);
}

console.log('validating feature score vs solver effort on a sample...');
const SAMPLE = 200, SCAP = 40000;
const feats = [], effort = [];
for (let i = 0; i < SAMPLE; i++) {
  const n = 1 + Math.floor((i / SAMPLE) * MAX);
  if (n === UNSOLVABLE) continue;
  const r = solve(n, SCAP);
  feats.push(featureScore(n));
  effort.push(r.solved ? Math.log(r.nodes + 1) : Math.log(SCAP) + 1); // capped -> hardest
}
const rho = spearman(feats, effort);
console.log(`Spearman rank correlation (feature score vs solver nodes): ${rho.toFixed(3)}  (n=${feats.length})`);

// ---- score all 32000 and bucket into quartile tiers ----
console.log('scoring all 32000 deals...');
const scores = new Array(MAX + 1);
for (let n = 1; n <= MAX; n++) scores[n] = (n === UNSOLVABLE) ? null : featureScore(n);
const sorted = [];
for (let n = 1; n <= MAX; n++) if (scores[n] != null) sorted.push(scores[n]);
sorted.sort((a, b) => a - b);
const q = p => sorted[Math.floor(p * sorted.length)];
const t1 = q(0.25), t2 = q(0.50), t3 = q(0.75); // quartiles -> Leicht/Mittel/Schwer/Experte

let out = '';
for (let n = 1; n <= MAX; n++) {
  let t;
  if (n === UNSOLVABLE) t = 9;
  else { const v = scores[n]; t = v <= t1 ? 1 : v <= t2 ? 2 : v <= t3 ? 3 : 4; }
  out += String(t);
}
const js = `/* adFreeCell - ESTIMATED difficulty tiers for deals 1..32000.
 * Generated by tools/estimate-difficulty.js from static opening-layout features
 * (buried aces / low cards, blocked foundations, initial ordered runs), bucketed
 * into quartiles. This is a fast estimate, not a solver-verified rating.
 * Rank correlation with solver effort on a sample: rho=${rho.toFixed(3)}.
 * Tier per deal (1-indexed string): 1 Leicht, 2 Mittel, 3 Schwer, 4 Experte, 9 unlösbar.
 */
window.Difficulty = { estimated: true, rho: ${rho.toFixed(3)}, tiers: "${out}" };
`;
fs.writeFileSync(path.join(__dirname, '..', 'js', 'difficulty.js'), js);
const counts = { 1: 0, 2: 0, 3: 0, 4: 0, 9: 0 };
for (const ch of out) counts[ch]++;
console.log('wrote js/difficulty.js  tiers:', counts);
